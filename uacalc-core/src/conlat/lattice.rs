    fn generate_tuples(&self, size: usize, arity: usize) -> Vec<Vec<usize>> {
        if arity == 0 {
            return vec![vec![]];
        }

        let mut tuples = Vec::new();
        let mut current = vec![0; arity];

        loop {
            tuples.push(current.clone());

            // Generate next tuple
            let mut i = arity - 1;
            while i < arity {
                current[i] += 1;
                if current[i] < size {
                    break;
                }
                current[i] = 0;
                if i == 0 {
                    break;
                }
                i -= 1;
            }

            if i == 0 && current[0] == 0 {
                break;
            }
        }

        tuples
    }

